import json
from pathlib import Path


def read_json(path: Path) -> dict | list:
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except (FileNotFoundError, json.JSONDecodeError):
        return {}


def markdown_cell(value: object) -> str:
    text = str(value or "Unknown").replace("\n", " ").strip()
    return text.replace("|", "\\|")


def friction_metadata(path: Path) -> dict[str, str]:
    metadata: dict[str, str] = {
        "id": path.stem,
        "persona": "Unknown",
        "style": "Unknown",
        "shard": "Unknown",
        "status": "open",
        "summary": "",
    }
    content = path.read_text(encoding="utf-8")
    lines = content.splitlines()

    for line in lines[:24]:
        stripped = line.strip().strip("*")
        if ":" not in stripped:
            continue
        key, value = stripped.split(":", 1)
        key = key.strip().lower().replace(" ", "_")
        value = value.strip().strip("*").strip()
        if key in metadata and value:
            metadata[key] = value
        elif key == "surface" and value:
            metadata["shard"] = value
        elif key == "component" and value:
            metadata["shard"] = value

    for heading in ("## Problem", "**Problem:**", "**Description:**"):
        if heading in content:
            after = content.split(heading, 1)[1]
            for line in after.splitlines():
                summary = line.strip().strip("-").strip()
                if summary:
                    metadata["summary"] = summary
                    break
            break

    if not metadata["summary"]:
        for line in lines:
            stripped = line.strip()
            if stripped and not stripped.startswith("#") and ":" not in stripped:
                metadata["summary"] = stripped
                break

    return metadata


def write_runs_rollup(repo_root: Path) -> None:
    output_file = repo_root / ".jules" / "index" / "generated" / "RUNS_ROLLUP.md"
    runs = collect_runs(repo_root)

    output_file.parent.mkdir(parents=True, exist_ok=True)
    with output_file.open("w", encoding="utf-8", newline="\n") as f:
        f.write("# Generated Run Index\n\n")
        f.write("This file is automatically generated by `.jules/bin/build_index.py`.\n")
        f.write(
            "It rolls up metadata from all run packets in `.jules/runs/` "
            "and historical ledgers.\n\n"
        )

        f.write("| Run ID | Persona | Style | Shard | Status | Gates Run | Source |\n")
        f.write("|---|---|---|---|---|---|---|\n")

        for r in runs:
            f.write(
                f"| `{r['id']}` | {markdown_cell(r['persona'])} | "
                f"{markdown_cell(r['style'])} | {markdown_cell(r['shard'])} | "
                f"{markdown_cell(r['status'])} | {r['gates_run']} | "
                f"{markdown_cell(r['source'])} |\n"
            )


def write_friction_rollup(repo_root: Path) -> None:
    output_file = repo_root / ".jules" / "index" / "generated" / "FRICTION_ROLLUP.md"
    friction_dir = repo_root / ".jules" / "friction" / "open"
    items = []
    if friction_dir.exists():
        items = [
            friction_metadata(path)
            for path in sorted(friction_dir.glob("*.md"), key=lambda path: path.name)
        ]

    output_file.parent.mkdir(parents=True, exist_ok=True)
    with output_file.open("w", encoding="utf-8", newline="\n") as f:
        f.write("# Generated Friction Index\n\n")
        f.write("This file is automatically generated by `.jules/bin/build_index.py`.\n")
        f.write("It rolls up active friction metadata from `.jules/friction/open/`.\n\n")

        f.write("| ID | Persona | Style | Shard | Status | Summary |\n")
        f.write("|---|---|---|---|---|---|\n")

        for item in items:
            f.write(
                f"| `{markdown_cell(item['id'])}` | {markdown_cell(item['persona'])} | "
                f"{markdown_cell(item['style'])} | {markdown_cell(item['shard'])} | "
                f"{markdown_cell(item['status'])} | {markdown_cell(item['summary'])} |\n"
            )


def collect_runs(repo_root: Path) -> list[dict[str, object]]:
    runs = []

    # 1. Live runs
    runs_dir = repo_root / ".jules" / "runs"
    if runs_dir.exists():
        for run_path in sorted(runs_dir.iterdir(), key=lambda path: path.name):
            if not run_path.is_dir():
                continue

            envelope = read_json(run_path / "envelope.json")
            if not isinstance(envelope, dict):
                envelope = {}
            result = read_json(run_path / "result.json")
            if not isinstance(result, dict):
                result = {}

            runs.append(
                {
                    "id": run_path.name,
                    "persona": envelope.get("persona") or "Unknown",
                    "style": envelope.get("style") or "Unknown",
                    "shard": envelope.get("primary_shard") or "Unknown",
                    "status": result.get("status") or "in-progress",
                    "gates_run": len(result.get("gates_run", [])),
                    "source": "live",
                }
            )

    # 2. Historical Docs Ledger
    docs_ledger_path = repo_root / ".jules" / "docs" / "ledger.json"
    docs_ledger = read_json(docs_ledger_path)
    if isinstance(docs_ledger, list):
        for entry in docs_ledger:
            if not isinstance(entry, dict):
                continue

            run_id = entry.get("run_id", "Unknown")
            env_path = repo_root / ".jules" / "docs" / "envelopes" / f"{run_id}.json"
            env_data = read_json(env_path) if env_path.exists() else {}
            if not isinstance(env_data, dict):
                env_data = {}

            persona = (
                env_data.get("persona")
                or entry.get("persona")
                or entry.get("lane")
                or "Unknown"
            )
            style = env_data.get("style") or entry.get("style") or "Unknown"
            shard = env_data.get("primary_shard") or entry.get("target") or "Unknown"
            gates_run = len(env_data.get("receipts", entry.get("receipts", [])))

            runs.append(
                {
                    "id": run_id,
                    "persona": persona,
                    "style": style,
                    "shard": shard,
                    "status": "historical",
                    "gates_run": gates_run,
                    "source": "docs",
                }
            )

    # 3. Historical Quality Ledger
    quality_ledger_path = repo_root / ".jules" / "quality" / "ledger.json"
    quality_ledger = read_json(quality_ledger_path)
    if isinstance(quality_ledger, dict) and "runs" in quality_ledger:
        for entry in quality_ledger["runs"]:
            if not isinstance(entry, dict):
                continue

            run_id = entry.get("run_id", "Unknown")
            env_path = repo_root / ".jules" / "quality" / "envelopes" / f"{run_id}.json"
            env_data = read_json(env_path) if env_path.exists() else {}
            if not isinstance(env_data, dict):
                env_data = {}

            desc = entry.get("description", "")
            persona = env_data.get("persona") or "Unknown"
            if persona == "Unknown" and "Gatekeeper" in desc:
                persona = "Gatekeeper"

            style = env_data.get("style") or "Unknown"
            shard = env_data.get("primary_shard") or entry.get("target") or "Unknown"
            gates_run = len(env_data.get("receipts", entry.get("receipts", [])))

            runs.append(
                {
                    "id": run_id,
                    "persona": persona,
                    "style": style,
                    "shard": shard,
                    "status": "historical",
                    "gates_run": gates_run,
                    "source": "quality",
                }
            )

    return runs


def main() -> None:
    repo_root = Path(__file__).resolve().parents[2]
    write_runs_rollup(repo_root)
    write_friction_rollup(repo_root)


if __name__ == "__main__":
    main()
